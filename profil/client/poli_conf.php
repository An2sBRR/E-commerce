                <div class="d-flex justify-content-center"><h2>Politique de confidentialité</h2></div>

</br>
<p>Nous accordons une grande importance à la protection de vos données personnelles et à la confidentialité de vos informations. Cette politique de confidentialité explique comment <strong><em>nous recueillons, utilisons et partageons vos données personnelles</strong></em>lorsque vous utilisez notre site e-commerce.</p>
<strong>Collecte de données personnelles</strong>
<p>Nous recueillons les données personnelles que vous nous fournissez lorsque vous créez un compte sur notre site e-commerce, lorsque vous passez une commande, lorsque vous contactez notre service clientèle ou lorsque vous vous inscrivez à notre newsletter. Ces données peuvent inclure votre nom, votre adresse e-mail, votre adresse de livraison, votre numéro de téléphone, vos informations de paiement et d'autres informations que vous choisissez de nous fournir.</p>
<strong>Utilisation des données personnelles</strong>
<p>Nous utilisons vos données personnelles pour traiter vos commandes, pour communiquer avec vous au sujet de vos commandes, pour personnaliser votre expérience d'achat sur notre site e-commerce et pour vous envoyer des offres promotionnelles ou des informations sur nos produits et services. Nous pouvons également utiliser vos données personnelles pour améliorer notre site e-commerce, pour prévenir la fraude et pour assurer la sécurité de nos utilisateurs.</p>
<strong>Partage des données personnelles</strong>
<p>Nous ne partageons pas vos données personnelles avec des tiers, sauf si cela est nécessaire pour traiter vos commandes ou si nous sommes légalement tenus de le faire. Nous pouvons partager vos données personnelles avec nos fournisseurs de services tiers qui nous aident à traiter les paiements, à expédier les commandes et à fournir des services de support clientèle.</p>
<strong>Sécurité des données personnelles</strong>
<p>Nous prenons des mesures de sécurité raisonnables pour protéger vos données personnelles contre les accès non autorisés, les pertes ou les modifications. Toutefois, nous ne pouvons garantir la sécurité absolue de vos données personnelles.</p>
<strong>Droit d'accès, de rectification et de suppression des données personnelles</strong>
<p>Vous avez le droit d'accéder à vos données personnelles que nous avons enregistrées, de les corriger si elles sont inexactes ou de demander leur suppression. Si vous avez des questions ou des préoccupations concernant notre politique de confidentialité ou la gestion de vos données personnelles, veuillez nous contacter à l'adresse e-mail fournie sur notre site e-commerce.</p>

<em>Nous pouvons mettre à jour cette politique de confidentialité de temps à autre. Nous vous informerons de toute modification importante de cette politique de confidentialité par e-mail ou par une notification sur notre site e-commerce.</em>
