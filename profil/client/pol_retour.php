  <li>Utilisez votre étiquette de retour dans les 45 jours qui suivent l’achat, nous prendrons en charge les frais de livraison.</li>
  <li>La première étiquette de retour est gratuite par commande. Pour plus d'une étiquette de retour sur la même commande, les frais de retour seront de 4,5 € par colis supplémentaire, qui seront déduits dans votre remboursement.</li>
  <li>Vous pouvez aussi faire le retour via votre transporteur local mais à vos frais.</li>
</ol>
